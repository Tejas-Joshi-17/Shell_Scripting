#!/bin/bash

for num in 1 2 3
do
  echo "Number is :- $num"
done
# Number is :- 1
# Number is :- 2
# Number is :- 3


for name in Raju Sham Ram
do
  echo "Name is :- $name"
done
# Name is :- Raju
# Name is :- Sham
# Name is :- Ram


for number in {1..5}
do
  echo "Number is :- $number"
done
# Number is :- 1
# Number is :- 2
# Number is :- 3
# Number is :- 4
# Number is :- 5
